// Function to redirect the user to the login page
function redirectToLoginPage() {
    // Change the current URL to the '/login' route
    // This will load the login page
    window.location.href = "/login";
}

// Function to redirect the user to the registration page
function redirectToRegisterPage() {
    // Change the current URL to the '/register' route
    // This will load the registration page
    window.location.href = "/register";
}
